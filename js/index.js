function loadProject() {
  const projectName = window.location.search.split("=")[1];
  const body = document.getElementById("container");
  const project = projects[projectName];

  const title = document.getElementById("title");
  title.className = "title-container";
  const name = document.createElement("h1");
  name.textContent = project.name;
  name.className = "name";
  title.appendChild(name);

  const description = document.querySelector(".description");
  description.innerText = project.description;
  if (!project.description) {
    const aboutHeader = document.querySelector("#about-project");
    aboutHeader.style.visibility = "hidden";
  }

  const shortDescription = document.createElement("p");
  shortDescription.textContent = project.shortDescription;
  shortDescription.className = "short-description";
  title.appendChild(shortDescription);

  let isAnyActive = false;
  const carousel = document.getElementById("carousel-inner");
  project.images.forEach((imageUrl) => {
    const itemContainer = document.createElement("div");
    if (!isAnyActive) {
      itemContainer.classList.add("carousel-item", "active");
      isAnyActive = true;
    } else {
      itemContainer.className = "carousel-item";
    }

    const image = document.createElement("img");
    image.classList.add("d-block", "w-100");
    image.src = imageUrl;
    itemContainer.appendChild(image);
    carousel.appendChild(itemContainer);
  });
}
function loadProjects() {
  const list = document.getElementById("projects-list");

  Object.keys(projects).forEach((name) => {
    console.log(name);
    const project = projects[name];
    const projectPreview = document.createElement("a");
    projectPreview.href = `/project.html?name=${name}`;
    projectPreview.className = "project-preview-link";

    const previewImageContainer = document.createElement("div");
    previewImageContainer.className = "preview-image-container";

    const previewImage = document.createElement("img");
    previewImage.src = project.images[0];

    const previewTitle = document.createElement("h5");
    previewTitle.className = "preview-title";
    previewTitle.textContent = project.name;

    const previewShortDescription = document.createElement("p");
    previewShortDescription.className = "preview-short-description";
    previewShortDescription.textContent = project.shortDescription;

    previewImageContainer.appendChild(previewImage);
    projectPreview.appendChild(previewImageContainer);
    const title = document.createElement("span");
    title.className = "title";
    title.innerHTML = project.name;
    previewImageContainer.appendChild(title);
    // projectPreview.appendChild(previewTitle);
    // projectPreview.appendChild(previewShortDescription)
    const listItem = document.createElement("li");
    listItem.className = "project-preview";
    listItem.appendChild(projectPreview);
    list.appendChild(listItem);
  });
}

function writeMenu() {
  var iSpeed = 60; // time delay of print out

  const typewriter = (text, elementId, wait) => {
    var destination = document.getElementById(elementId);
    text.split("").forEach((letter) => {
      const letterEl = document.createElement("span");
      letterEl.textContent = letter;
      letterEl.style.visibility = "hidden";
      destination.appendChild(letterEl);
    });
    setTimeout(() => {
      destination.children.forEach((letter, i) => {
        setTimeout(() => {
          letter.style.visibility = "visible";
        }, i * iSpeed);
      });
    }, wait);
  };

  typewriter("Projekty", "projects-link", 18 * iSpeed);
  typewriter("Pracownia", "workshop-link", 27 * iSpeed);
  typewriter("Kontakt", "contact-link", 36 * iSpeed);

  projectsLink = document.getElementById("projects-link");
  projectsLink.addEventListener("click", () => {
    window.scrollTo({ top: window.innerHeight, left: 0, behavior: "smooth" });
  });
}
